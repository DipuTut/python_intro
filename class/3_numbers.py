# Definiere Zahlen
# Ganze Zahlen
number1 = 11
number2 = 5

# Arithmetische Operationen mit den Zahlen
# Addition
sum = number1 + number2
print(sum)
# Subtraction
sub = number1 - number2
print(sub)
# Multiplication
mult = number1 * number2
print(mult)
# Division: Ergebnis ist immer eine Gleitkommazahl
div = number1 / number2
print(div)
# Potnzierung (Exponentiation)
power = number1 ** number2
print(power)
# Modulo: den Rest der Division berechnen
modulo = number1 % number2
print(modulo)
# Ganzzahlige Division: Ergebnis ist eine ganze Zahl
intdiv = number1 // number2
print(intdiv)

# Gleitkommazahlen
float1 = 10.25
float2 = 5.66

float_result = (float1 + float2) * 10
print(float_result)